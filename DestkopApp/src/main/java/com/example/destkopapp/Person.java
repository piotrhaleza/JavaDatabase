package com.example.destkopapp;


public class Person {

    public Person(int id,String name, String surname, int age, String address) {
        this.id = id;
        this.name = name;
        this.surname = surname;
        this.age = age;
        this.address = address;
    }
    public int id;
    public String name;
    public String surname;
    public int age;
    public String address;
}
